                            <p>If you wonder how facilities recycle E-waste, then here's how:</p>
                            <ul class="list-group ms-4 mb-3">
                                <li class="list-group-item">Collect e-waste: The first step is to collect e-waste from households, businesses, and organizations. This can be done through e-waste recycling drives or by partnering with e-waste recycling companies.</li>
                                <li class="list-group-item">Sort and segregate: The collected e-waste needs to be sorted and segregated into different categories based on the type of material and the level of hazard.</li>
                                <li class="list-group-item">Dismantle and separate: The next step is to dismantle the e-waste and separate the different components such as metals, plastics, and circuit boards.</li>
                                <li class="list-group-item">Recover and reuse: The recovered materials can be reused in the manufacturing of new products. For example, metals such as copper and gold can be used in the production of new electronic devices.</li>
                                <li class="list-group-item">Dispose of hazardous materials: The hazardous materials such as lead, cadmium, and mercury need to be disposed of safely and properly to prevent environmental pollution and human health hazards.</li>
                            </ul>
                            <p class="card-text ms-4">By following these steps, we can make use of recycling e-waste and reduce the environmental impact of electronic waste while conserving valuable resources.</p>
