                            <h2>An Overview of the E-Waste Growing Issue</h2>
                            <p class="ms-3 lead">
                                The world is facing a growing e-waste problem. The amount of e-waste generated each year is increasing at an alarming rate, and the ammount of E-waste recycled is having a minimal impact.
                                According to a report from Statista, over 50 million metric tons of E-waste is generated globally every year, and what's worse is that only around 17% is of generated E-waste is properly recycled.
                            </p>
                            <div class="cite-reference">
                                <p>Statista. (2023). Global E-Waste - Statistics & Facts. Retrieved from <a href="https://www.statista.com/topics/3409/electronic-waste-worldwide/#topicOverview" target="_blank" rel="noopener">https://www.statista.com/topics/3409/electronic-waste-worldwide/#topicOverview</a></p>
                                <p class="cite-source">Source: The Roundup (2023)</p>
