                                <h2 class="text-start">What is E-waste?</h2>
                                <p class="lead">The term E-Waste refers to any electronic products or equipment that have outlived their usefulness. For example, broken old phones, TVs, computers, printers, faxes, and many other electronics that are no longer needed or deemed useless. E-waste contains bits of precious metal such as gold, copper, silver, palladium, and nickel, which could be retrieved when processed properly.
                                </p>
                                <h2 class="mt-5">Why is it such a big problem?</h2>
                                <p class="lead">
                                    Improper disposal of E-waste causes severe environmental damage, as it is often left for scrap, burned, processed by hand, or illegally dumped. E-waste contains toxic chemicals that can poison the soil and water, and if not disposed of properly, can lead to serious health issues such as cancers, miscarriages, brain and liver damage, and other illnesses.
                                </p>
